export function countryIds(names: string[]): string[] {
  const out: string[] = [];
  for (const n of names) {
    const id = ID[n];
    if (id && !out.includes(id)) out.push(id);
  }
  return out;
}

export function hasMappable(names: string[]): boolean {
  return names.some((n) => ID[n]);
}
